def aumentar(valor, aumento):
    resultado = valor * (1 + (aumento/100))
    return resultado


def diminuir(valor, reduz):
    resultado = valor * (1 - (reduz/100))
    return resultado


def dobro(valor):
    resultado = valor * 2
    return resultado


def metada(valor):
    resultado = valor * 0.5
    return resultado
